public class MathMethods {
    public static void main(String[] args){
        System.out.println(Math.abs(4 + -7 - 10));//13
        System.out.println(Math.abs(-19.8));//19.8
        System.out.println(Math.pow(5, 2));//25.0
        System.out.println(Math.pow(5, 2.0));//25.0
        System.out.println(Math.ceil(98.1));//99.0
        System.out.println(Math.floor(76.9));//76.0
        System.out.println(Math.max(12, 90));//90
        System.out.println(Math.min(120, 90.1));//90.1
        System.out.println(Math.sqrt(36));//6.0
        System.out.println(Math.sqrt(29 + 37));//8.12403
        System.out.println(Math.log(Math.pow(Math.E, 4)));//4.0
        System.out.println(Math.round(4.8));//5

    }
}
